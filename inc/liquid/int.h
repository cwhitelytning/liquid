/**
 * @file int.h
 * @brief Header file containing integer typedefs.
 */

#ifndef LIQUID_INT_H
#define LIQUID_INT_H

/**
 * @typedef uchar_t
 * @brief Typedef for unsigned char.
 */
typedef unsigned char uchar_t;

/**
 * @typedef ushort_t
 * @brief Typedef for unsigned short.
 */
typedef unsigned short ushort_t;

/**
 * @typedef uint_t
 * @brief Typedef for unsigned int.
 */
typedef unsigned int uint_t;

/**
 * @typedef ullong_t
 * @brief Typedef for unsigned long long.
 */
typedef unsigned long long ullong_t;

/**
 * @typedef schar_t
 * @brief Typedef for signed char.
 */
typedef signed char schar_t;

/**
 * @typedef sshort_t
 * @brief Typedef for signed short.
 */
typedef signed short sshort_t;

/**
 * @typedef sint_t
 * @brief Typedef for signed int.
 */
typedef signed int sint_t;

/**
 * @typedef sllong_t
 * @brief Typedef for signed long long.
 */
typedef signed long long sllong_t;

#endif // LIQUID_INT_H
